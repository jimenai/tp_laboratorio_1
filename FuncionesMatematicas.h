/** \brief Recibe dos numeros enteros y calcula la suma de ambos
 *
 * \param int Primer numero entero
 * \param int Segundo numero entero
 * \return int La suma de los numeros
 *
 */
int sumarNumeros(int, int);

/** \brief Recibe dos numeros enteros y calcula la resta entre ambos
 *
 * \param int Primer numero entero
 * \param int Segundo numero entero
 * \return int La resta entre el primer numero y el segundo
 *
 */
int restarNumeros(int, int);

/** \brief Recibe dos numeros enteros y los multiplica
 *
 *
 * \param int Primer numero entero
 * \param int Segundo numero entero
 * \return int El producto de ambos numeros
 *
 */
int multiplicarNumeros(int, int);

/** \brief  Recibe dos numeros enteros, divide el primer numero sobre el segundo numero
 *
 * \param int Primer numero entero
 * \param int Segundo numero entero
 * \return float El cociente del los dos numeros
 *
 */
float dividirNumeros(int, int);
